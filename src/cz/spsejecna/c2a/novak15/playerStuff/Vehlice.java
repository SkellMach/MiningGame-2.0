package cz.spsejecna.c2a.novak15.playerStuff;

public class Vehlice {
	String name;
	int buildingSpeed;
	int lvl;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getBuildingSpeed() {
		return buildingSpeed;
	}

	public void setBuildingSpeed(int buildingSpeed) {
		this.buildingSpeed = buildingSpeed;
	}

	public int getLvl() {
		return lvl;
	}

	public void setLvl(int lvl) {
		this.lvl = lvl;
	}

}
