package cz.spsejecna.c2a.novak15.playerStuff;

public class MagicBuff {
	String name;
	String buff;
	int lvl;
	
	public String getBuff() {
		return buff;
	}

	public void setBuff(String buff) {
		this.buff = buff;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getLvl() {
		return lvl;
	}

	public void setLvl(int lvl) {
		this.lvl = lvl;
	}
	 
}
